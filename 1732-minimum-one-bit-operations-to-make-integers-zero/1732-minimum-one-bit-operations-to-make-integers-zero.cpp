typedef long long ll;

class Solution {
public:
    // 0000 -> 0 -> 0000
    // 0001 -> 1 -> 0000
    // 0010 -> 3 -> 0001
    // 0011 -> 2 -> 0001
    
    // 0100 -> 7 -> 0011
    // 0101 -> 6 -> 0011
    // 0110 -> 4 -> 0010
    // 0111 -> 5 -> 0010

    // 4 5 6 7
    // 6 7 4 5

    // 1000 -> 15 -> 0111
    // 1001 -> 14 -> 0111
    // 1010 -> 12 -> 0110
    // 1011 -> 13 -> 0110
    
    // 1100 -> 8  -> 0100
    // 1101 -> 9  -> 0100
    // 1110 -> 11 -> 0101
    // 1111 -> 10 -> 0101

    int minimumOneBitOperations(int _n) {
        int res = 0;
        int n = _n;
        while(n > 0){
            res ^= n;
            n >>= 1;
        }
        return res;
    }
};